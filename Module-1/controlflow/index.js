// Javascript Conntrol flow table:
// | Tool                       | Use                        |
// | -------------------------- | -------------------------- |
// | `if`, `else if`, `else`    | Make decisions             |
// | `switch`                   | Handle multiple conditions |
// | `for`, `while`, `do while` | Repeat code (loops)        |
// | `break`, `continue`        | Control loops              |
// | `return`                   | Exit a function early      |

// 1) if else :
let age = 18;

if (age < 18) {
  console.log("You are underage.");
} else if (age === 18) {
  console.log("Just turned adult!");
} else {
  console.log("You are an adult.");
}



